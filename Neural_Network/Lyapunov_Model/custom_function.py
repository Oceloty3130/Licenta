import torch

def custom_sin(x):
    return torch.sin(x)

def custom_cos(x):
    return torch.cos(x)

def custom_tanh(x):
    return torch.tanh(x)

def custom_cosh(x):
    return torch.cosh(x)

def custom_sinh(x):
    return torch.sinh(x)

def custom_mul(x,y):
    return x * y

def custom_pow(x,y):
    return x ** y

def custom_div(x,y):
    return x / y